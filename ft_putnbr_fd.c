#include "libft.h"

void	ft_putnbr_fd(int n, int fd)

{
	char	c;
	long	a;

	a = n;
	if (a < 0)
	{
		write(fd, "-", 1);
		a *= -1;
	}
	if (a >= 10)
		ft_putnbr_fd(a / 10, fd);
	c = (a % 10) + '0';
	write(fd, &c, 1);
}
